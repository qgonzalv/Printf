#include "libft.h"

int		ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb > 2147483647)
		return (0);
	if (nb < 2)
		return (0);
	if (nb >= 2)
	{
		while (i < nb && i < 46341)
		{
			if (nb % i == 0)
			{
				return (0);
			}
			i++;
		}
	}
	return (1);
}
