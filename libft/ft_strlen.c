#include "libft.h"
#include <string.h>
#include <stdio.h>

size_t	ft_strlen(char const *s)
{
	int i;

	i = 0;
	if (s == NULL)
		return (0);
	while (s[i] != '\0')
		i++;
	return (i);
}
